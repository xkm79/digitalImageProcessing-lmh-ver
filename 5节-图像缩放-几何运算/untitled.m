% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
data=guidata(hObject);
str1=get(handles.edit1,'String');
str2=get(handles.edit2,'String');
fx=str2double(str1);
fy=str2double(str2);
I2=data.I1;
[h w] = size(data.I1);
for i=1:1:w/3
    for j=1:1:h
        I2(j,i,1)=0;I2(j,i,2)=0;I2(j,i,3)=0;
    end
end
T=[fx 0 0;
   0 fy 0;
   0 0 1];%设定缩放矩阵
T=T^-1;
for i=1:1:w/3
  for j=1:1:h
     OLocation = [i;j;1];%组合原始位置矩阵
        NLocation= int16( T*OLocation );%计算目标位置矩阵
        if ( (NLocation(1)>1)&&(NLocation(1)<w/3)&&(NLocation(2)>1)&&(NLocation(2)<h) )%预防坐标溢出图像数组范围
          I2(j,i,1)=data.I1( NLocation(2),NLocation(1),1 );%红色分量缩放
          I2(j,i,2)=data.I1( NLocation(2),NLocation(1),2 );%绿色分量缩放
          I2(j,i,3)=data.I1( NLocation(2),NLocation(1),3 );%蓝色分量缩放
        end
   end
end
axes(handles.axes1);
imshow(I2);
data.I1=I2;
guidata(hObject,data);
